class Solution:
    def sum(self, num1: int, num2: int) -> int:
        """
        Given two integers num1 and num2, return the sum of the two integers.
        
        Args:
            num1 (int): The first integer
            num2 (int): The second integer
            
        Returns:
            int: The sum of num1 and num2
        """
        return num1 + num2
    